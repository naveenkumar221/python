# List Introduction.....
groceries = ["milk", "bread", "eggs"]
groceries.append("butter")
print(groceries)

# Tuples in Python......
coordinates = (20.5, 80.6)
print(coordinates[0])

# Set in Python......
fruits = {"apple", "banana", "apple", "orange"}
print(fruits)     # {'apple', 'banana', 'orange'}

# Dictionary in Python.....
person = {"name": "Naveen", "age": 25, "city": "Hyderabad"}
print(person["city"])

# Type Conversion in Python.......
print(int("10")  )     # '10' → 10
print(str(25) )        # 25 → '25'
print(list("hello"))   # 'hello' → ['h', 'e', 'l', 'l', 'o']
print(tuple([1, 2]) )         # [1, 2] → (1, 2)

